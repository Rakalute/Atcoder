def main():
    N = int(input())

    if 1000 <= N <= 999999:
        ans = N - 999
    elif 1000000 <= N <= 999999999:
        ans = 2 * (N - 999999) + 999 * (10 ** 3)
    elif 1000000000 <= N <= 999999999999:
        ans = 3 * (N - 999999999) + 2 * 999 * (10 ** 6) + 999 * (10 ** 3)
    elif 1000000000000 <= N <= 999999999999999:
        ans = 4 * (N - 999999999999) + 3 * 999 * (10 ** 9) + 2 * 999 * (10 ** 6) + 999 * (10 ** 3)
    elif N >= 1000000000000000:
        ans = 5 * (N - 999999999999999) + 4 * 999 * (10 ** 12) + 3 * 999 * (10 ** 9) + 2 * 999 * (10 ** 6) + 999 * (10 ** 3)
    else:
        ans = 0

    print(ans)



